            <p>Jakarta, CNBC Indonesia - Indonesia Polling Stations (IPS) kembali merilis survei terbaru terkait elektabilitas calon presiden seusai pelaksanaan debat pilpres. Hasilnya, Prabowo Subianto-Gibran Rakabuming Raka berada di posisi teratas disusul Anies-Cak Imin dan Ganjar-Mahfud.</p>
            <p>Survei terbaru IPS ini dilakukan pada periode 7-13 Januari 2024 di 38 provinsi dengan total 1.220 responden. Pengambilan sampel dilakukan dengan teknik acak bertingkat atau multistage random sampling.</p>
            <p>Survei dilakukan dengan teknik wawancara telepon dengan bantuan kuesioner. Margin of error survei +/- 2,8 persen dengan tingkat kepercayaan 95 persen.

Dalam survei, responden diberi pertanyaan 'Ketika kami tanyakan, jika saat ini dilaksanakan pemilihan presiden wakil presiden, siapakah yang akan ibu, bapak atau saudara pilih?'</p>
            <p>Peneliti Indonesia Polling Stations (IPS) Alfin Sugianto mengatakan sebanyak 51,8 persen responden memilih pasangan Prabowo-Gibran. Sedangkan AMIN mendapatkan 21,3 persen dan Ganjar Mahfud 19,2 persen.</p>
            <p>"Mayoritas publik atau sebanyak 51,8 persen responden mengaku akan memilih pasangan Prabowo-Gibran jika pilpres dilaksanakan hari ini, sedangkan 21,3 persen responden mengaku memilih Anies-Cak Imin, dan 19,2 persen responden memilih Ganjar-Mahfud. Sisanya tidak menjawab pertanyaan yang kami ajukan," kata Alfin dalam paparannya, Selasa (16/1/2024).</p>
            <p>Alfin menilai, menurunnya elektabilitas AMIN dan Ganjar-Mahfud lantaran melakukan penyerangan ke kandidat lain dalam debat capres 7 Januari lalu. Sementara itu, elektabilitas Prabowo-Gibran disebut semakin naik.</p>
            <p>"Strategi menyerang secara kolektif yang dilancarkan Anies Baswedan dan Ganjar Pranowo pada debat capres 7 Januari 2024 ternyata menjadi bumerang. Elektabilitas paslon Anies-Muhaimin dan Ganjar-Mahfud semakin merosot. Sementara elektabilitas pasangan Prabowo-Gibran justru kian melesat," kata Alfin.</p>
            <p>Menurut Alfin, jika strategi menyerang kandidat lain terus dilakukan, Pilpres 2024 dapat dimenangkan Prabowo-Gibran.
</p>
